//Callback Functions
//A callback function is a function that is passed as an argument to another function. It's invoked within the outer function when a certain condition is met or a specific task is completed.

//Key Characteristics:

//Passed as an argument: Callback functions are passed as arguments to other functions.
//Asynchronous operations: They are often used in asynchronous operations, such as making API calls, handling events, or performing time-consuming tasks.
//Invocation within the outer function: The outer function calls the callback function at the appropriate time.
//Common Use Cases:

//Asynchronous programming: Callback functions are essential for handling asynchronous operations in JavaScript, such as AJAX requests, file I/O, and timers.
//Event handling: They are used to respond to user interactions, such as button clicks or form submissions.
//Customizable behavior: Callback functions allow you to provide custom logic that will be executed when a certain event occurs or a task is completed.